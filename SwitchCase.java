public class SwitchCase
	{
		public static void main(String args[])
		{
		int x =100;
		switch(x)
		{
			case 10:
				System.out.println("Ten");
				break;
			case 100:
				System.out.println("Hundred");
				break;
			case 1000:
				System.out.println("Thousand");
				break;
			default:
				System.out.println("NA");
				break;
		}
		}
}